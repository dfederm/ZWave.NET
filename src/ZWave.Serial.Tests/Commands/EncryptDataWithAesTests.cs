using ZWave.Serial.Commands;

namespace ZWave.Serial.Tests.Commands;

[TestClass]
public class EncryptDataWithAesTests : CommandTestBase
{
    private record EncryptDataWithAesResponseData(ReadOnlyMemory<byte> OutputData);

    [TestMethod]
    public void Request()
        => TestSendableCommand(
            DataFrameType.REQ,
            CommandId.EncryptDataWithAes,
            new[]
            {
                (
                    Request: EncryptDataWithAesRequest.Create(
                        key: new byte[] { 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10 },
                        inputData: new byte[] { 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F, 0x20 }),
                    ExpectedCommandParameters: new byte[]
                    {
                        0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10,
                        0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F, 0x20,
                    }
                ),
            });

    [TestMethod]
    public void Response()
        => TestReceivableCommand<EncryptDataWithAesResponse, EncryptDataWithAesResponseData>(
            DataFrameType.RES,
            CommandId.EncryptDataWithAes,
            new[]
            {
                (
                    CommandParameters: new byte[] { 0xA1, 0xA2, 0xA3, 0xA4, 0xA5, 0xA6, 0xA7, 0xA8, 0xA9, 0xAA, 0xAB, 0xAC, 0xAD, 0xAE, 0xAF, 0xB0 },
                    ExpectedData: new EncryptDataWithAesResponseData(
                        OutputData: new byte[] { 0xA1, 0xA2, 0xA3, 0xA4, 0xA5, 0xA6, 0xA7, 0xA8, 0xA9, 0xAA, 0xAB, 0xAC, 0xAD, 0xAE, 0xAF, 0xB0 })
                ),
            });
}
